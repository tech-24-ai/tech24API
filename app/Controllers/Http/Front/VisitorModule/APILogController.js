  /**
   * Show a list of all iplogs.
   * GET iplogs
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async index({ request, response, view }) {
    const query = ApiLog.query();
    query.leftJoin("visitors", "visitors.id", "api_logs.user_id");
    query.leftJoin("vendors", "vendors.id", "api_logs.vendor_id");
    let page = 1;
    let pageSize = 100;

    if (request.input("page")) {
      page = request.input("page");
    }
    if (request.input("pageSize")) {
      pageSize = request.input("pageSize");
    }

    const search = request.input("search");
    const orderBy = request.input("orderBy");
    const orderDirection = request.input("orderDirection");
    const searchQuery = new Query(request, { order: "id" });

    if (orderBy && orderDirection) {
      query.orderBy(`api_logs.${orderBy}`, orderDirection);
    }
    query.orderBy(`api_logs.created_at`, "desc");
    if (search) {
      query.where(searchQuery.search(searchInFields));
    }
    query.select(`api_logs.created_at`)
    query.select(`api_logs.api_type`)
    query.select(`visitors.name`)
    query.select(`vendors.name as vendor_name`)
    if (request.input("filters")) {
      const filters = JSON.parse(request.input("filters"));
      filters.forEach((filter) => {
        switch (filter.name) {
          case "created_at":
            query.whereRaw(
              typeof filter.value == "object"
                ? `DATE(${filter.name}) between '${filter.value.startDate}' and '${filter.value.endDate}'`
                : `DATE(${filter.name}) = '${moment(filter.value).format(
                    "YYYY-MM-DD"
                  )}'`
            );
            break;
          case "updated_at":
            query.whereRaw(
              typeof filter.value == "object"
                ? `DATE(${filter.name}) between '${filter.value.startDate}' and '${filter.value.endDate}'`
                : `DATE(${filter.name}) = '${moment(filter.value).format(
                    "YYYY-MM-DD"
                  )}'`
            );
            break;
          default:
            query.whereRaw(`${filter.name} LIKE '%${filter.value}%'`);
            break;
        }
      });
    }

    const result = await query.paginate(page, pageSize);

    return response.status(200).send(result);
  }

